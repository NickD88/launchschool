# Which of the following are objects in Ruby? If they are objects, how can you find out what class they belong to?
#

# They are all objects.  You find what class they belong to buy the .class method.

true.class                      # TrueClass
"hello".class                   # String Class
[1, 2, 3, "happy days"].class   # Array Class
142.class                       # Fixnum Class
